public class operators {
	public static void main(String[]args) {
	//assignment operators
	int a=6;
	int b=20;
	//arithmetic operators
	int c=a+b;
	int d=b-a;
	int e=a*b;
	int f=b/a;
	int g=a%b;
	System.out.println(c);
	System.out.println(d);
	System.out.println(e);
	System.out.println(f);
	System.out.println(g);
	}

}
