package com.instagram.login;

public class Operatorssecond {
	public static void main(String[]args) {
		//post increment
		int a=10;
		System.out.println(a);
		int b= a++;
		System.out.println(a);
		System.out.println(b);
		//post decrement
		int c=30;
		System.out.println(c);
		int d=c--;
		System.out.println(c);
		System.out.println(d);
		
		//pre increment
		int e=10;
		System.out.println(e);
		int f=++e;
		System.out.println(e);
		System.out.println(f);
		//pre decrement
		int g=10;
		System.out.println(g);
		int h=--g;
		System.out.println(g);
		System.out.println(h);
		
		//string:-after string all values are considered as string
		System.out.println(10+20+30+"wxy"+"abc"+40+50);
		
	}

}
